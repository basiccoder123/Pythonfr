x = 10
y = 20
z = 30

avg_speed = (x+y+z)/3
print("The average speed is:", avg_speed)

if x < avg_speed:
    print("x is lower than average speed.")
elif y < avg_speed:
    print("y is lower than average speed.")
elif z < avg_speed:
    print("z is lower than average speed.")
else:
    print("All are above the average speed.")